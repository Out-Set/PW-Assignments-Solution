class Question2 {
	public static void main(String[] args) {
		
		// Pre Increment
		int b = 5;
		System.out.println(++b); // 6
		System.out.println(b);   // 6
		System.out.println();

		// Post Increment
		int a = 5;
		System.out.println(a++); // 5
		System.out.println(a);   // 6
		System.out.println();

		
		// Pre Decrement
		int c = 5;
		System.out.println(--c); // 4
		System.out.println(c);   // 4
		System.out.println();

		// Post Decrement
		int d = 5;
		System.out.println(d--); // 5
		System.out.println(d);   // 4	
		System.out.println();
	}
}